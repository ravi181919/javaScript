const month = 3 
switch (month)
{
    case 1:
        console.log('january');
        break;
    case 2:
        console.log('fab');
        break;
    case 3:
        console.log('march');
        break;
    case 4:
        console.log('april');
        break;
    default:
        console.log(`no match`); 
        break;   
};


